/* 
Maior que (>): Retorna True se o valor à esquerda for maior que o valor à direita.

Exemplo: 5 > 3 resulta em True.

Menor que (<): Retorna True se o valor à esquerda for menor que o valor à direita.

Exemplo: 3 < 5 resulta em True.

Maior ou igual a (>=): Retorna True se o valor à esquerda for maior ou igual ao valor à direita.

Exemplo: 5 >= 5 resulta em True.

Menor ou igual a (<=): Retorna True se o valor à esquerda for menor ou igual ao valor à direita.

Exemplo: 3 <= 5 resulta em True.

Igual a (==): Retorna True se os dois valores forem iguais.

Exemplo: 5 == 5 resulta em True.

Diferente de (!=): Retorna True se os valores forem diferentes.

Exemplo: 5 != 3 resulta em True.
----------------------------------------------------------------------------------
AND (and): Retorna True se ambas as condições forem verdadeiras.

Exemplo: True and False resulta em False.

OR (or): Retorna True se pelo menos uma das condições for verdadeira.

Exemplo: True or False resulta em True.

NOT (not): Inverte o valor lógico da expressão.

Exemplo: not True resulta em False.


*/